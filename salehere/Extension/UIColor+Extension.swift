import UIKit

extension UIColor {

    static func darkYellow() -> UIColor {
        let red: CGFloat = 233.0 / 255.0
        let green: CGFloat = 164.0 / 255.0
        let blue: CGFloat = 30.0 / 255.0

        return UIColor(red: red, green: green, blue: blue, alpha: 1.0)
    }
}
